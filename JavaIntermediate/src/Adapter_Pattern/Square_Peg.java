package Adapter_Pattern;

public class Square_Peg {

    private double width;

    public Square_Peg(double width) {
        this.width = width;
    }

    public double getWidth() {
        return width;
    }

    public double getSquare() {
        double result;
        result = Math.pow(this.width, 2);
        return result;
    }
}

